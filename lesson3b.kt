fun main(){
    // while loop 
    var count =1
    while (count <=10){
        println("The counter is $count")
        // increment 
        count =count +1
    }
    //loop all years from 2000 to 3000
    var year=2000
    while(year<=3000){
        println("the year is $year")
        // increment 
        year=year+1
    }
    // loop all odd number from 10-50 
    var odd=11
    while(odd<50){
        println("the odd number is $odd")
        // increment 
        odd=odd+2
    }

}