fun main() {
    val day=7
    if (day==1){
        println("Sunday")
    }
    else if(day==2){
        println("Monday")
    }
    else if(day==3){
        println("Tuesday")
    }
    else if(day==4){
        println("Wednesday")
    }
    else if(day==5){
        println("Thursday")
    }
    else if(day==6){
        println("Friday")
    }
    else if(day==7){
        println("Saturday")
    }
    else {
        println("Invalid day")
    }
}